    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String bookid = bid.getText();
        try{
            String sql = "SELECT * FROM Book WHERE Book_ID='"+bookid+"'";
            stmt = con.prepareStatement(sql);
            rs = stmt.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("Name");
                bname.setText(add1);
                String add2 = rs.getString("Edition");
                bedition.setText(add2);
                String add3 = rs.getString("Publisher");
                bpublisher.setText(add3);
                String add4 = rs.getString("Price");
                bprice.setText(add4);
                String add5 = rs.getString("Pages");
                bpages.setText(add5);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                stmt.close();
                rs.close();
            }catch(Exception e){
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

